    public function configureDashboard(): Dashboard
    {
        return Dashboard::new()
            ->setTitle('Back');
    }

    public function configureMenuItems(): iterable
    {
        yield MenuItem::linktoRoute('Back to the website', 'fas fa-home', 'homepage');
        yield MenuItem::linkToCrud('Organization', 'fas fa-map-marker-alt', Organization::class);
        yield MenuItem::linkToCrud('Group', 'fas fa-comments', Group::class);
        yield MenuItem::linkToCrud('Container', 'fas fa-map-marker-alt', Container::class);
        yield MenuItem::linkToCrud('Contract', 'fas fa-map-marker-alt', Contract::class);
        yield MenuItem::linkToCrud('Sensor', 'fas fa-map-marker-alt', Sensor::class);
    }
